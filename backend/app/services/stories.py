"""Themed sets / mini-stories (Batch 2).

A mini-story is a short, themed sequence of linked exercises that share a single dev-world
narrative (e.g. "a day debugging a flaky deploy"). The narrative scaffolding — title, intro and
per-beat narration — is curated *here in code*, not produced by the model. Each beat's exercise is
generated by the existing validated per-type generators (`grammar.generate_exercise`), sharing the
scenario as `context`, so quality stays reliable on a small local model and the story structure is
independent of the model in use.

Grading is unchanged: every beat carries its own Fernet-sealed token graded by `POST /check`.
"""

from __future__ import annotations

import random
from typing import Any

from . import grammar

# Curated scenarios. `topics` must use the canonical backend topic names (grammar.TOPICS); a beat
# whose topic isn't recognised falls back to the generator's weighted pick. `narration` has one line
# per beat (we serve STORY_LEN beats), threading the story between exercises. `context` is the shared
# scenario string fed to every beat's generator.
STORY_LEN = 3

SCENARIOS: list[dict[str, Any]] = [
    {
        "id": "flaky-deploy",
        "title": "The flaky deploy",
        "intro": "Friday, 5 p.m. The deploy is green on staging but the production rollout keeps "
        "failing. You and the on-call engineer start digging.",
        "context": "a developer debugging a flaky production deploy on a Friday evening",
        "topics": ["conditionals", "verb sequence (tense agreement)", "modal verbs"],
        "narration": [
            "The pipeline goes red again. You read the error log together.",
            "You roll back and trace what happened just before the failure.",
            "The on-call asks what you should do before the next deploy.",
        ],
    },
    {
        "id": "code-review",
        "title": "The code review",
        "intro": "Your pull request is up. A senior engineer leaves a wall of comments — most of "
        "them about clarity, not logic.",
        "context": "a developer responding to comments in a code review",
        "topics": ["articles", "prepositions", "word order"],
        "narration": [
            "The first comment asks you to rename a vague variable.",
            "The next one is about a confusing sentence in your doc comment.",
            "The senior suggests reordering the steps in your README.",
        ],
    },
    {
        "id": "onboarding-day",
        "title": "Onboarding day",
        "intro": "First day at a new startup. Your buddy walks you through the stack over coffee "
        "and a very long wiki page.",
        "context": "a developer on their first day learning a new codebase and team",
        "topics": ["phrasal verbs", "prepositions", "gerunds & infinitives"],
        "narration": [
            "Your buddy explains how the services hang together.",
            "You set up your laptop and run into the usual env issues.",
            "By lunch you're ready to pick up your first small ticket.",
        ],
    },
    {
        "id": "incident-call",
        "title": "The 3 a.m. page",
        "intro": "PagerDuty wakes you up. The API is throwing 500s and the dashboards are all red. "
        "You join the incident call half awake.",
        "context": "a developer handling a late-night production incident on a call",
        "topics": ["verb sequence (tense agreement)", "modal verbs", "comparatives & superlatives"],
        "narration": [
            "You describe what the monitoring showed when it started.",
            "The team weighs two possible fixes against each other.",
            "You agree on the safest option and write the post-mortem note.",
        ],
    },
]


async def build_story(level: str | None = None, context_override: str | None = None) -> dict[str, Any]:
    """Assemble a mini-story: pick a curated scenario, then generate one exercise per beat.

    Each exercise is produced by `grammar.generate_exercise` (its own 3-retry + multiple-choice
    fallback), so a single bad generation degrades to a simpler card rather than breaking the set.
    `level` is locked across the whole story; the per-beat topic varies across the scenario's topics
    so the set isn't monotonous. `context_override`, if given, replaces the scenario's flavor context.
    """
    scenario = random.choice(SCENARIOS)
    context = context_override or scenario["context"]
    topics = scenario["topics"]

    beats: list[dict[str, Any]] = []
    for i in range(STORY_LEN):
        topic = topics[i % len(topics)]
        exercise = await grammar.generate_exercise(
            topic=topic, level=level, ex_type=None, context=context
        )
        narration = scenario["narration"][i] if i < len(scenario["narration"]) else ""
        beats.append({"narration": narration, "exercise": exercise})

    return {
        "id": scenario["id"],
        "title": scenario["title"],
        "intro": scenario["intro"],
        "level": grammar._cefr(level),
        "beats": beats,
    }
